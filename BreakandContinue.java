class BreakandContinue
{
public static void main(String arg[])
{
System.out.println("Break Statement:");
for(int i=1;i<=5;i++)
{
if(i==4) break;
System.out.println(i);
}
System.out. println("Continue Statement:");
for(int i=1;i<=5;i++)
{
if(i==1) continue;
System.out.println(i);
}
}
}
